var age = 19;

// Primitive Javascript Data Types:

// Number: floating point numbers, for decimals and integers
// String: Sequence of characters, used for text
// Boolean: logical datatype: true or false
// Undefined: Datatype of a variable that does not hava a value yet
// Null: Non-existent

// Javascript has dynamic typing: the interpreter assigns variables a type at runtime based on the variable's value at the time.

// Boolean
var fullAge = true;
console.log(fullAge);

//Convention to use camelcasing

//Undefined: just declare and not assign
